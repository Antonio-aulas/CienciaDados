  private void btnPrecoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrecoActionPerformed

if (rbHam.isSelected()){
  preco=13.50;
}
if (rbChees.isSelected()){
  preco=15.20;
}
if (rbEgg.isSelected()){
  preco=14.30;
}
if (rbMonstro.isSelected()){
  preco=25.00;
}
if (cbMonstro.isSelected()){
  preco=preco+12;
}
if (cbOvo.isSelected()){
  preco=preco+3;
}
if (cbBacon.isSelected()){
  preco=preco+4;
}
if (cbQueijo.isSelected()){
  preco=preco+3.8;
}

 JOptionPane.showMessageDialog (null, "Valor a Pagar "+ Double.toString(preco));

  }//GEN-LAST:event_btnPrecoActionPerformed

